package com.tuxdev.sampleaacjava.model;

public class Data{
	private String usia;
	private String lahir;
	private String nama;
	private String ultah;
	private String zodiak;

	public void setUsia(String usia){
		this.usia = usia;
	}

	public String getUsia(){
		return usia;
	}

	public void setLahir(String lahir){
		this.lahir = lahir;
	}

	public String getLahir(){
		return lahir;
	}

	public void setNama(String nama){
		this.nama = nama;
	}

	public String getNama(){
		return nama;
	}

	public void setUltah(String ultah){
		this.ultah = ultah;
	}

	public String getUltah(){
		return ultah;
	}

	public void setZodiak(String zodiak){
		this.zodiak = zodiak;
	}

	public String getZodiak(){
		return zodiak;
	}
}
